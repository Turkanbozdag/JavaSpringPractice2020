package day02_Variables;

public class EscapeSquences {
    public static void main(String[] args) {
        System.out.println("\tturkan");
        System.out.println("Bozdag");
        // \t means a pragraph space
        System.out.println("cybertek school");
        System.out.println("cybertek \nschool");
        System.out.println("my\nschoolname\nis\ncybertek");// \n means new line, starts with new line
    // print: my favorite tv show is "nothing"
        System.out.println("my favorite tv show is\"nothing\"");
                                    //|" means double quote,prints
        System.out.println("\\");
        // print single\\ on console
        System.out.println("/");
        System.out.println("my favorite book is\' java\'");// \'prints single
        System.out.println("my favorite book is 'java'");//print single

    }
}
