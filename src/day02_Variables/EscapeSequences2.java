package day02_Variables;


public class EscapeSequences2 {

    public static void main(String[] args) {
        System.out.println("Aydin");
        System.out.println("\t\t\t\t\tAydin");// \t paragraph space
        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println();
        System.out.println("my\nname\n is");
        // \n start new line
        System.out.println("My favorite movie is\" the walking dead\" ");// \" means double quote
        System.out.println("\\");
        System.out.println("/");// / print single quote
        System.out.println("my favorite book is \'java\'");
        System.out.println("my favorite book is'java'");// \' print single quote or '

    }

}
