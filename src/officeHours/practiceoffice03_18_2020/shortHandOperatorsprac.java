package officeHours.practiceoffice03_18_2020;

public class shortHandOperatorsprac {
    public static void main(String[] args) {
        // +=:addition assigment
        int a=100;
        //a=a+200;//both same mean
        a+=200;
        System.out.println(a);//300
        a+=200*3;
        System.out.println(a);
        //-=substraction
        int x=5;
        x*=3;//x=x*3 =15
        System.out.println(x);
        x*=2+1;//x=x*3
        System.out.println(x);//45

        // /=:division
        int n=900;
        n/=3;//n/=3;
        System.out.println(n);//300

        n/=3*5;//n=n/15
        System.out.println(n);//20
        int b =900;
        b/=1+3*2/3;
        System.out.println(b);


        //% remainder
        // remainde:numerator-(denominator*whole number for resul
        int m=10;
        m=m%3;
        System.out.println(m);
        int y=300;
        y%=3*5;
        System.out.println(y);// y%=15;y=%a5=0







    }






}
